import mongoose from 'mongoose';

// Определение схемы
const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
  },
});

// Создание модели
const User = mongoose.model('User', userSchema);

export default User;